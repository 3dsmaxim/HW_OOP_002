public abstract class ActionsExpressions {

    void Difference(int a, int b) {

        System.out.println(a + " - " + b + " = " + (a - b));

    }

    void Division(int a, int b) {

        System.out.println(a + " / " + b + " = " + (a / b));

    }

    void Multiplication(int a, int b) {

        System.out.println(a + " * " + b + " = " + (a * b));

    }

    void Summ(int a, int b) {

        System.out.println(a + " + " + b + " = " + (a + b));

    }

}
